    </header>
    <div class="entry-content">
        <p>
            DisCon III, the 79th World Science Fiction Convention, announced the winners of the 2021 Hugo Awards at a
            ceremony in Washington, DC, USA on the evening of Saturday, December 18, 2021.
        </p>
        <p>
            BEST NOVEL: Network Effect, Martha Wells (Tor.com)
        </p>
        <p>
            BEST NOVELLA: The Empress of Salt and Fortune, Nghi Vo (Tor.com)
        </p>
        <p>
            BEST NOVELETTE: Two Truths and a Lie, Sarah Pinsker (Tor.com)
        </p>
        <p>
            BEST SHORT STORY: “Metal Like Blood in the Dark”, T. Kingfisher (Uncanny Magazine, September/October 2020)
        </p>
        <p>
            BEST SERIES: The Murderbot Diaries, Martha Wells (Tor.com)
        </p>
