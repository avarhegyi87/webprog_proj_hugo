    <div class="entry-content">
        <p>
            The Hugo Awards, first presented in 1953 and presented annually since 1955, are science fiction’s most prestigious award. 
            The Hugo Awards are voted on by members of the World Science Fiction Convention (“Worldcon”), 
            which is also responsible for administering them.
        </p>
        <p>
            The Hugo Awards are trademarked by the World Science Fiction Society (“WSFS”), 
            an unincorporated literary society which sponsors the annual World Science Fiction Convention (“Worldcon”) and the Hugo Awards. 
            WSFS is really just a framework for the individual Worldcons — it has no officers and no permanent organization 
            (other than the Mark Protection Committee, which is responsible for registering and defending WSFS’s trademarks, 
            and its Marketing Subcommittee, which runs this website.)
        </p>
        <p>
            The Hugo process is as follows:
        </p>
        <ol>
            <li>
                During January-March each year members of the Worldcon are each allowed to nominate 
                up to five people or works from the previous year in fifteen categories.
            </li>
            <li>
                In early April a shortlist is announced of five finalists in each category 
                and a final ballot is sent to members of the Worldcon that year.
            </li>
            <li>
                The members of the current Worldcon cast a final ballot, with balloting ending around sometime in July 
                (it depends on exactly when Worldcon is that year.) 
                The ballot is a preferential ballot which allows the voters to rank all nominees.
            </li>
            <li>
                At Worldcon the winners are announced at the Hugo Ceremony and receive a special trophy.
            </li>
        </ol>
        <p>
            We have a lot more information about the Hugos at our FAQ.
        </p>
