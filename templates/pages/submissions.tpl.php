        <h2>How do I submit my book (or story or movie) for nomination for a Hugo Award?</h2>
        <p>
            The short answer is, “you can’t”. If the Hugos were judged by a small panel, as is the case with some other awards, then you could send your work to the award jury. But the Hugo Awards nomination process is open to every member of the current and previous Worldcon. That means that the “jury” often consists of thousands of people. You don’t want to send your work to all of them, even assuming you could.
        </p>
        <p>
            <b>There is no submission process for the Hugo Award.</b> You do not have to register your work with anyone to be eligible. There is no submission or entry fee. The Hugo voters are good at finding and nominating good works, and do talk among themselves, so word spreads. There are also third-party web sites where people can make recommendations for Hugo Awards. Some of these sites are listed on the sidebar at the right. (Listing here does not imply official endorsement. If you know of a site that accumulates recommendations, let us know so we can add it to the list.)
        </p>
