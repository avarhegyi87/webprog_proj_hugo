                <h1 class="entry-title">2022 Hugo Award Finalists Announced</h1>
            </header>
            <div class="entry-content">
                <p>
                    Chicon 8, the 80th World Science Fiction Convention, announced on April 7, 2022 the finalists for
                    the
                    2022
                    Hugo Awards, Lodestar Award for Best Young Adult Book, and Astounding Award for Best New Writer. The
                    full
                    list of finalists is on the 2022 Hugo Awards page.
                </p>
                <p>
                    There were 1368 valid nominating ballots (1366 electronic and 2 paper) received and counted from the
                    members
                    of the 2021 and 2022 World Science Fiction Conventions for the 2022 Hugo Awards. Voting on the final
                    ballot
                    will open at a date in May 2022 to be announced.
                </p>
                <p>
                    Only Chicon 8 members will be able to vote on the final ballot and choose the winners for the 2022
                    Awards.
                    If you are not already a member, you can join the convention through the Chicon 8 website. You must
                    be
                    at
                    least a supporting member in order to participate in the Hugo Awards.
                </p>
                <p>
                    More information about the 2022 Hugo Awards is available on the Chicon 8 website Hugo Awards page.
                    Questions
                    about the 2022 Hugo Awards process should be directed to hugo-help@discon3.org. Please do not submit
                    questions to the Hugo Awards website, as we do not administer the Hugo Awards and can only pass your
                    questions on to the award administrators.
                </p>
            </div>
        </article>

